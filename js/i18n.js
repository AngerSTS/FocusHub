  // Language display names for the selector
  const LANGS = [
    {code:'en',name:'English'},{code:'nl',name:'Nederlands'},{code:'es',name:'Español'},
    {code:'pt',name:'Português'},{code:'fr',name:'Français'},{code:'de',name:'Deutsch'},
    {code:'hi',name:'हिंदी'},{code:'zh',name:'中文'},
  ];

  let current = 'en';

  /** Get translation for key, fallback to English */
  function t(key) {
    return (T[current] && T[current][key]) || (T.en && T.en[key]) || key;
  }

  /** Apply translations to all elements with data-i18n attribute */
  function apply() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = t(el.getAttribute('data-i18n-ph'));
    });
    document.documentElement.lang = current;
  }

  /** Detect browser language and set closest match */
  function detect() {
    const bl = (navigator.language || navigator.userLanguage || 'en').slice(0, 2).toLowerCase();
    if (T[bl]) current = bl;
  }

  function set(lang) { if (T[lang]) { current = lang; apply(); } }
  function get() { return current; }
  function langs() { return LANGS; }

  return { t, apply, detect, set, get, langs };
